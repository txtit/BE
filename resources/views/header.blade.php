    // Cập nhật giao diện giỏ hàng
    function updateCartUI() {
        cartItemsList.innerHTML = "";

        if (cart.length === 0) {
            cartItemsList.innerHTML = "<li class='text-gray-500'>Giỏ hàng trống</li>";
            return;
        }

        cart.forEach((item, index) => {
            const li = document.createElement("li");
            li.classList.add("flex", "justify-between", "items-center", "border-b", "pb-2");

            li.innerHTML = `
                <span>${item.name} x ${item.quantity} </span>
                <span>${(item.price * item.quantity).toLocaleString()}đ</span>
                <button class="text-red-500 remove-item" data-index="${index}">❌</button>
            `;

            cartItemsList.appendChild(li);
        });

        // Xóa sản phẩm khỏi giỏ hàng
        document.querySelectorAll(".remove-item").forEach(button => {
            button.addEventListener("click", function() {
                const index = this.getAttribute("data-index");
                cart.splice(index, 1);
                updateCartUI();
            });
        });
    }
    // Cập nhật tổng tiền
    function updateTotal() {
        let total = 0;
        document.querySelectorAll('.border-b').forEach(item => {
            const price = parseInt(item.querySelector('p.text-gray-600').innerText.replace('đ', '').replace(
                '.',
                ''));
            const quantity = parseInt(item.querySelector('.quantity').innerText);

            // console.log(price)
            console.log(quantity)
            total += price * quantity;
        });

        document.getElementById('totalPrice').innerText = total.toLocaleString() + 'đ';
    }
